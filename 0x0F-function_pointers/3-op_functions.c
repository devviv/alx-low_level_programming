/**
* op_sub - substract
* @a : an integer
* @b : an integer
* Return: dif
*/

int op_sub(int a, int b)
{
	int dif;

	dif = (a) - (b);
	return (dif);
}
/**
* op_add - add
* @a : an integer
* @b : an integer
* Return: sum
*/

int op_add(int a, int b)
{
	int sum;

	sum = a + b;
	return (sum);
}
/**
* op_mul - multiplicate
* @a : an integer
* @b : an integer
* Return: mul
*/

int op_mul(int a, int b)
{
	int mul;

	mul = (a) * (b);
	return (mul);
}
/**
* op_div - divided
* @a : an integer
* @b : an integer
* Return: div
*/

int op_div(int a, int b)
{
	int div;

	div = (a) / (b);
	return (div);
}


/**
* op_mod - modulo
* @a : an integer
* @b : an integer
* Return: mod
*/

int op_mod(int a, int b)
{
	int mod;

	mod = (a) % (b);
	return (mod);
}
